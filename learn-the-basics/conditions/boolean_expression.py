# Variable assignment is done using a "single equals" operator "="
X = 2

# Whereas comparison between two variables is done
# using the "double equals" operator "=="
print("(X == 2) = %s" % (X == 2))

# The "not equals" operator is marked as "!="
print("(X != 2) = %s" % (X != 2))

print("(X < 3)  = %s" % (X < 3))
