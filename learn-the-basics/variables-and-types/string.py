# With a single quote
my_string = 'hello'
print(my_string)

# Or a double quotes
my_string = "hello"
print(my_string)

# The difference between the two is that
# using double quotes makes it easy to include apostrophes
# (whereas these would terminate the string if using single quotes)
my_string = "Don't worry about apostrophes"
print(my_string)
